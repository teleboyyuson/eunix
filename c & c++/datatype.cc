#include <iostream>
using namespace std;

int main() {
    int i = 1;
    float f = 2.5f;
    double d = 3.14159;
    char c = 'C';
    bool b = false;

    cout << "int: " << i << endl;
    cout << "float: " << f << endl;
    cout << "double: " << d << endl;
    cout << "char: " << c << endl;
    cout << "bool: " << b << endl;

    return 0;
}
